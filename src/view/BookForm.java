    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (FormHelper.validateTextFields(this.jPanel)) {
            String isbn = this.txtIsbn.getText();
            String title = this.txtTitle.getText();
            String authors = this.txtAuthors.getText();
            String pubDate = this.txtPubDate.getText();
            double price = (Double.valueOf(this.txtPrice.getText()));
            String type = this.comboType.getSelectedItem().toString();
            int stock = (Integer.valueOf(this.txtStock.getText()));
            String publisher = this.comboPub.getSelectedItem().toString();

            if (type.matches("Paper")) {
                double postageCost = (Double.valueOf(this.txtPostCost.getText()));
                BookShopController.newBook(isbn, title, authors, pubDate, price,
                        postageCost, type, stock, publisher);
            } else {
                String format = this.txtFormat.getText();
                List licences = new ArrayList();
                licences.addAll(Arrays.asList(this.txtAreaLicences.getText().split("\\n")));
                BookShopController.newBook(isbn, title, authors, pubDate, price,
                        format, licences, type, stock, publisher);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

