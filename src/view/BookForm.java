    /**
     * Sets the lists of publishers comboBox
     */
        public void setPudCombo() {
        publishers = BookShopController.listPublishers();
        for (Object name : publishers) {
            this.comboPub.addItem(name.toString());
        }
    }
    
    /**
     *  Sets all data fields to data in the editData array
     * @param type
     */
    public void setDataFields(String type) {
        this.txtIsbn.setText(editData.get(0).toString());
        this.txtTitle.setText(editData.get(1).toString());
        this.txtAuthors.setText(editData.get(2).toString());
        this.txtPubDate.setText(editData.get(3).toString());
        this.txtPrice.setText(editData.get(4).toString());
        this.txtStock.setText(editData.get(5).toString());
        this.comboPub.getModel().setSelectedItem(editData.get(6).toString());
        
        if (type.matches("Paper")) {
            this.txtPostCost.setText(editData.get(7).toString());
            this.comboType.setSelectedItem("Paper");
        } else {
            this.txtFormat.setText(editData.get(7).toString());
            for (Object l : (ArrayList) editData.get(8)) {
                this.txtAreaLicences.append(l.toString() + "\n");
            }
            this.comboType.setSelectedItem("Electronic");
        }
        
        cl.show(layerPane, type);
    }
