    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (FormHelper.validateTextFields(this.jPanel)) {
            int orderID = Integer.valueOf(this.txtOrderID.getText());
            String orderDate = this.txtOrderDate.getText();
            String deliveryDate = "";
            double cost = Double.valueOf(this.txtCost.getText());
            String publisher = this.comboPub.getModel().getSelectedItem().toString();

            DefaultTableModel model = (DefaultTableModel) this.tblItems.getModel();
            String[][] items = new String[model.getRowCount()][2];
            for (int count = 0; count < model.getRowCount(); count++) {
                items[count][0] = BookShopController.retreiveBookIsbn(model.getValueAt(count, 0).toString());
                items[count][1] = model.getValueAt(count, 1).toString();
            }

            BookShopController.newOrder(orderID, orderDate, deliveryDate, cost,
                    publisher, items);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

