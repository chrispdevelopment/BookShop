    /**
     * Sets the lists of publishers comboBox
     */
    public void setPudCombo() {
        publishers = BookShopController.listPublishers();
        for (Object name : publishers) {
            this.comboPub.addItem(name.toString());
        }
    }

    /**
     * Sets all data fields to data in the editData array
     */
    public void setDataFields() {
        this.txtOrderID.setText(editData.get(0).toString());
        this.txtOrderDate.setText(editData.get(1).toString());
        this.txtCost.setText(editData.get(3).toString());
        this.comboPub.getModel().setSelectedItem(editData.get(5).toString());
        this.comboPub.setEnabled(false);
    }

    /**
     * Sorts the table model before adding it to the table
     *
     * @param defaultModel
     */
    public void sortTable(DefaultTableModel defaultModel) {
        TableModel model = new SortingTableModel(defaultModel, 0);

        this.tblItems.setModel(model);
    }

    /**
     * Removes the selected row from the table model
     *
     * @param model
     */
    public void removeSelectedRows(DefaultTableModel model) {
        int[] rows = this.tblItems.getSelectedRows();
        for (int i = 0; i < rows.length; i++) {
            model.removeRow(rows[i] - i);
        }
    }
