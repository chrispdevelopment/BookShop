    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (FormHelper.validateTextFields(this.jPanel) == true) {
            String name = this.txtName.getText();
            String email = this.txtEmail.getText();
            String phoneNo = this.txtPhone.getText();
            List address = new ArrayList();
            address.addAll(Arrays.asList(this.txtAreaAddress.getText().split("\\n")));

            BookShopController.newPublisher(name, email, phoneNo, address);
        }
    }//GEN-LAST:event_btnSaveActionPerformed
