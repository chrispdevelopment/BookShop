    //<editor-fold defaultstate="collapsed" desc="Methods for setting of form fields">
    /**
     * Sets all data fields to data in the editData array
     */
    public void setDataFields() {
        this.txtName.setText(editData.get(0).toString());
        this.txtEmail.setText(editData.get(1).toString());
        this.txtPhone.setText(editData.get(2).toString());
        for (Object l : (ArrayList) editData.get(3)) {
            this.txtAreaAddress.append(l.toString() + "\n");
        }
    }
